const MINIFIED_AVG_LINE_THRESHOLD = 500;

export function isGeneratedFile(content: string): boolean {
	const header = content.slice(0, 1000).toUpperCase();
	return GENERATED_FILE_MARKERS.some((marker) => header.includes(marker));
}

export function isMinifiedFile(content: string): boolean {
	const lineCount = content.split("\n").length;
	if (lineCount === 0) return false;
	return content.length / lineCount > MINIFIED_AVG_LINE_THRESHOLD;
}

function createIgnoreFilter(rootPath: string) {
	const ig = ignore();

	try {
		const gitignoreContent = readFileSync(join(rootPath, ".gitignore"), "utf-8");
		ig.add(gitignoreContent);
	} catch {
		// .gitignore doesn't exist — that's fine
	}

	return ig;
}

