export interface ScannedFile {
	filePath: string;
	language: string | null;
	content: string;
	contentHash: string;
	sizeBytes: number;
	lineCount: number;
}

export function scanRepository(clonePath: string): ScannedFile[] {
	const files: ScannedFile[] = [];
	const ig = createIgnoreFilter(clonePath);
	walkDirectory(clonePath, clonePath, files, ig);

	files.sort((a, b) => {
		const aIsPriority = isPriorityPath(a.filePath);
		const bIsPriority = isPriorityPath(b.filePath);
		if (aIsPriority && !bIsPriority) return -1;
		if (!aIsPriority && bIsPriority) return 1;
		return a.filePath.localeCompare(b.filePath);
	});

	if (files.length > config.maxFilesPerRepo) {
		return files.slice(0, config.maxFilesPerRepo);
	}

	return files;
}

function isPriorityPath(filePath: string): boolean {
	const parts = filePath.split("/");
	const first = parts[0];
	return ["src", "lib", "app", "pkg", "cmd", "internal"].includes(first);
}

function walkDirectory(
	rootPath: string,
	currentPath: string,
	files: ScannedFile[],
	ig: ReturnType<typeof ignore>,
): void {
	let entries: string[];
	try {
		entries = readdirSync(currentPath);
	} catch {
		return;
	}

	for (const entry of entries) {
		const fullPath = join(currentPath, entry);
		const relativePath = relative(rootPath, fullPath);

		let stat;
		try {
			stat = statSync(fullPath);
		} catch {
			continue;
		}

		if (stat.isDirectory()) {
			if (SKIP_DIRS.has(entry)) continue;
			if (ig.ignores(`${relativePath}/`)) continue;
			walkDirectory(rootPath, fullPath, files, ig);
			continue;
		}

		if (!stat.isFile()) continue;

		if (SKIP_FILES.has(entry)) continue;

		const ext = extname(entry).toLowerCase();
		if (SKIP_EXTENSIONS.has(ext)) continue;

		if (ig.ignores(relativePath)) continue;

		if (stat.size > config.maxFileSize) continue;
		if (stat.size === 0) continue;

		let content: string;
		try {
			const buffer = readFileSync(fullPath);
			// Check for binary content (null bytes in first 8KB)
			const sample = buffer.subarray(0, 8192);
			if (sample.includes(0)) continue;
			content = buffer.toString("utf-8");
		} catch {
			continue;
		}

		if (isGeneratedFile(content)) continue;
		if (isMinifiedFile(content)) continue;

		const language = LANGUAGE_MAP[ext] || null;

		files.push({
			filePath: relativePath,
			language,
			content,
			contentHash: createHash("sha256").update(content).digest("hex"),
			sizeBytes: stat.size,
			lineCount: content.split("\n").length,
		});
	}
}

export function buildFileTree(files: ScannedFile[]): string {
	const tree: Record<string, unknown> = {};

	for (const file of files) {
		const parts = file.filePath.split("/");
		let current = tree;
		for (let i = 0; i < parts.length - 1; i++) {
			if (!current[parts[i]]) {
				current[parts[i]] = {};
			}
			current = current[parts[i]] as Record<string, unknown>;
		}
		current[parts[parts.length - 1]] = null;
	}

	const lines: string[] = [];
	renderTree(tree, "", lines);
	return lines.join("\n");
}

function renderTree(node: Record<string, unknown>, indent: string, lines: string[]): void {
	const entries = Object.entries(node).sort(([a, av], [b, bv]) => {
		const aIsDir = av !== null;
		const bIsDir = bv !== null;
		if (aIsDir && !bIsDir) return -1;
		if (!aIsDir && bIsDir) return 1;
		return a.localeCompare(b);
	});

	for (const [name, value] of entries) {
		if (value === null) {
			lines.push(`${indent}${name}`);
		} else {
			lines.push(`${indent}${name}/`);
			renderTree(value as Record<string, unknown>, `${indent}  `, lines);
		}
	}
}
